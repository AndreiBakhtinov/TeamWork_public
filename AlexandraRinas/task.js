db.videos.aggregate([
    { // на входе вся коллекция видео
        $match: { duration_secs: { $lt: 120 * 60 } }
        // на выходе отфильтрованная колекция видео, где продолжительность до 2 часа
    },
    { // на входе отфильтрованная колекция видео
        $count: 'total_videos' 
     } // на выходе один документ с указанным количеством отфильтрованных видео, т.е. продолжительностью 2 часа
])